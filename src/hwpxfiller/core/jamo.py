"""자모(초/중/종성) 분해 부분일치 — 한글 검색의 링1 단일 유틸.

R-flow 합의문 결정 23("자모 분해 부분일치 = 산술 분해 + 겹받침 성분 확장·음절 역매핑
하이라이트") + 부록 B-6·B-7 의 이행. **사양 정본은 시안**
``docs/r-flow-mockups/block4-filter-crystallize-demo.html`` 의 ``jamoMap``/``jamoFind``
(522~546행) — 이 모듈은 그 의미론의 충실 이식이다.

## 왜 자모인가

한글 IME 는 음절을 점진 조립한다 — 「행복도시」를 치는 도중 화면의 질의는 「행복도ㅅ」이다.
음절 문자열 비교로는 이 중간 상태가 아무것도 못 찾는다(「행복도ㅅ」⊄「행복도시」). 질의와
대상을 모두 자모 열로 펴면(「행복도ㅅ」→ ``ㅎㅐㅇㅂㅗㄱㄷㅗㅅ``) 타이핑 중간 상태가 완성
음절의 접두가 되어 **치는 동안 이미 맞는다**(결정 23 의 「행복도ㅅ」 단계 매치).

## 세 계약

- **산술 분해** — 완성 음절(U+AC00~U+D7A3)만 초·중·종으로 편다(초 ⌊v/588⌋ · 중
  ⌊v%588/28⌋ · 종 v%28). 비음절 문자(숫자·라틴·낱자모·기호)는 그대로 통과 — 낱자모
  질의(조립 중인 ㅅ)가 분해된 종성/초성과 같은 평면에서 만난다.
- **겹받침 성분 확장** — 종성 ㄳ·ㄵ·ㄺ… 은 성분 낱자(ㄱㅅ·ㄴㅈ·ㄹㄱ…)로 편다. 「닭」을
  겨눠 「달」까지 쳤을 때(ㄷㅏㄹ ⊂ ㄷㅏㄹㄱ) 이미 맞게 하기 위함이다.
- **음절 역매핑** — 자모 열의 각 위치가 원문 몇 번째 문자에서 왔는지를 기억해, 매치
  구간을 **원문 문자 범위**로 되돌린다(하이라이트가 음절 단위로 칠해진다 — 부분 매치된
  음절은 통째로 포함).

## 경계(사양 밖 — 시안 동형, 조용한 확장 금지)

- **초성 검색 아님** — ``ㅎㅂㄷ`` 같은 초성 나열은 연속 자모 부분열이 아니라 매치되지
  않는다. 결정 23 은 부분일치이지 초성검색이 아니다.
- **대소문자 구분** — 비한글 문자는 원문 그대로 비교한다(시안 동형).
- **겹자모 낱자 질의 미확장** — 질의의 낱자 ㄳ 는 그대로 통과하므로 확장된 ``ㄱㅅ`` 와
  안 맞는다(IME 상 낱자 겹받침 단독 입력은 조립 중간에 나타나지 않는 형태라 수용).
- **정규화 불수행** — NFD(첫가끝 자모) 입력은 분해하지 않는다. 이 코퍼스는 Windows
  NFC 세계다.
- **인덱스 단위 = 파이썬 코드포인트**(시안과의 선언된 편차 — 고효율 리뷰 #1).
  시안 JS(``charCodeAt``)는 UTF-16 코드유닛 인덱스라 비-BMP 문자(이모지 등) 앞에서
  두 단위 세계가 갈라진다. 그래서 **매치 인덱스를 웹으로 건네는 것을 계약으로
  금지한다**: 하이라이트는 Python 이 이 범위로 원문을 **직접 잘라 세그먼트**(앞/매치/뒤)
  로 스냅샷에 싣고, 웹은 받은 조각을 그리기만 한다 — 번역 경계 자체를 소거한다
  (지배 결함류 "파생경계 번역오류"의 상류 차단, PR-2 배선 계약).

소비처: 블록 4 필터 부분일치(열 검색·전열 검색, PR-2)·블록 5 근사 판정(부록 B-7 —
:func:`decompose` 공유). 웹(js)에 병렬 구현을 두지 않는다 — 필터 판정은 상태 소유자
(Python)가 내리고 웹은 매치 범위를 받아 그리기만 한다(단일 출처).

이 모듈은 순수 코어다(Qt·웹 불가지, 상태 없음). 회귀 = ``tests/test_jamo.py``.
"""
from __future__ import annotations

__all__ = [
    "CHOSEONG",
    "JUNGSEONG",
    "JONGSEONG",
    "decompose",
    "jamo_find",
    "jamo_contains",
]

# 호환 자모(U+3131~) 테이블 — IME 가 조립 중간에 뱉는 낱자와 같은 문자 평면을 쓴다
# (첫가끝 U+1100 계열이면 낱자 질의와 영영 못 만난다). 시안 522~526행과 동일.
CHOSEONG = (
    "ㄱ", "ㄲ", "ㄴ", "ㄷ", "ㄸ", "ㄹ", "ㅁ", "ㅂ", "ㅃ", "ㅅ",
    "ㅆ", "ㅇ", "ㅈ", "ㅉ", "ㅊ", "ㅋ", "ㅌ", "ㅍ", "ㅎ",
)
JUNGSEONG = (
    "ㅏ", "ㅐ", "ㅑ", "ㅒ", "ㅓ", "ㅔ", "ㅕ", "ㅖ", "ㅗ", "ㅘ",
    "ㅙ", "ㅚ", "ㅛ", "ㅜ", "ㅝ", "ㅞ", "ㅟ", "ㅠ", "ㅡ", "ㅢ", "ㅣ",
)
# 종성 — 인덱스 0 = 받침 없음. 겹받침은 성분 낱자로 확장(ㄳ→ㄱㅅ …, 결정 23).
JONGSEONG = (
    "", "ㄱ", "ㄲ", "ㄱㅅ", "ㄴ", "ㄴㅈ", "ㄴㅎ", "ㄷ", "ㄹ", "ㄹㄱ",
    "ㄹㅁ", "ㄹㅂ", "ㄹㅅ", "ㄹㅌ", "ㄹㅍ", "ㄹㅎ", "ㅁ", "ㅂ", "ㅂㅅ", "ㅅ",
    "ㅆ", "ㅇ", "ㅈ", "ㅊ", "ㅋ", "ㅌ", "ㅍ", "ㅎ",
)

_SYL_BASE = 0xAC00  # 완성 음절 블록 시작 '가'
_SYL_LAST = 0xD7A3  # 끝 '힣'


def _decompose_map(text: str) -> "tuple[str, list[int]]":
    """자모 열 + 역매핑 — 자모 열 i번째 문자가 원문 ``pos[i]`` 번째 문자에서 왔다.

    완성 음절만 산술 분해하고 그 외(숫자·라틴·낱자모·기호·공백)는 1:1 통과한다.
    """
    jamo: "list[str]" = []
    pos: "list[int]" = []
    for i, ch in enumerate(text):
        code = ord(ch)
        if _SYL_BASE <= code <= _SYL_LAST:
            v = code - _SYL_BASE
            part = CHOSEONG[v // 588] + JUNGSEONG[(v % 588) // 28] + JONGSEONG[v % 28]
            for j in part:
                jamo.append(j)
                pos.append(i)
        else:
            jamo.append(ch)
            pos.append(i)
    return "".join(jamo), pos


def decompose(text: str) -> str:
    """원문을 자모 열로 편다(역매핑 없이) — 블록 5 근사 판정(B-7)의 공유 표면.

    역매핑이 필요 없는 경로(질의 분해·근사 판정)의 전용 루프다 — ``_decompose_map`` 을
    부르면 버려질 pos 리스트를 매번 할당한다(필터 핫패스는 키스트로크×행수라 낭비가
    곱해진다, 고효율 리뷰 #3). 질의 분해 상각(행 반복 전 1회 분해) API 는 소비처(블록 4
    필터 모델)의 형상이 확정될 때 그쪽 관문으로 붙인다 — 추측성 API 선설치 금지.
    """
    out: "list[str]" = []
    for ch in text:
        code = ord(ch)
        if _SYL_BASE <= code <= _SYL_LAST:
            v = code - _SYL_BASE
            out.append(CHOSEONG[v // 588])
            out.append(JUNGSEONG[(v % 588) // 28])
            out.append(JONGSEONG[v % 28])
        else:
            out.append(ch)
    return "".join(out)


def jamo_find(haystack: str, needle: str) -> "tuple[int, int] | None":
    """자모 평면 첫 매치를 **원문 문자 범위** ``(start, end)`` 로 반환(하이라이트용).

    범위는 반개구간 ``[start, end)`` — ``haystack[start:end]`` 가 칠할 구간이다. 매치가
    음절 중간에서 끝나면(「행복도ㅅ」이 「시」의 ㅅ까지만 문 경우) 그 음절 문자가 통째로
    구간에 들어간다(역매핑이 음절 단위라 자연 귀결 — 어색한 반쪽 하이라이트 없음).

    **단위 = 파이썬 코드포인트 인덱스** — 파이썬 슬라이싱 전용이다. 이 인덱스를 웹(JS,
    UTF-16 코드유닛 세계)으로 건네면 비-BMP 문자 앞에서 하이라이트가 밀린다 — 건네지
    말고 여기서 잘라 세그먼트로 실어라(모듈 경계 절·고효율 리뷰 #1).

    빈 질의는 ``None`` — "빈 질의 = 조건 없음"의 해석은 소비처(필터 모델)의 몫이고,
    유틸은 '아무데나 맞는 빈 매치'를 지어내지 않는다(시안 동형).
    """
    if not needle:
        return None
    hay_jamo, pos = _decompose_map(haystack)
    needle_jamo = decompose(needle)
    at = hay_jamo.find(needle_jamo)
    if at < 0:
        return None
    return pos[at], pos[at + len(needle_jamo) - 1] + 1


def jamo_contains(haystack: str, needle: str) -> bool:
    """부분일치 여부만 — 필터 판정 경로(범위 불요)의 지름길."""
    return jamo_find(haystack, needle) is not None
